/*******************************************************************************
	DB接続
--------------------------------------------------------------------------------
	$db = db_connect($info);
--------------------------------------------------------------------------------
	$db					DB接続リソース
	$info				DB接続情報
		conn				接続情報
			db					DB名
			host				ホスト名
			user				ユーザ名
			pswd				パスワード
			char				キャラクタ・セット
		error				エラー処理関数　func($msg, $sql)
								$msg	メッセージ
								$sql	SQL文
--------------------------------------------------------------------------------
　DBに接続してDB接続リソースを返す。
*******************************************************************************/
function db_connect($info)
{
	// エラー処理関数を調整
	if(empty($info['error'])) {
		$info['error'] = 'db_dummy';
	}

	// DBに接続
	$conn = $info['conn'];
	$db = mysqli_connect($conn['host'], $conn['user'],  $conn['pswd']);

	// 異常ならエラーを通知して終了
	if(!$db) {
		$info['error']('データベースに接続できませんでした。');
		return(NULL);
	}

	// DBを選択
	$ok = mysqli_select_db($db, $conn['db']);

	// 異常ならエラーを通知して終了
	if(!$ok) {
		$info['error']('データベースを選択できませんでした。');
		return(NULL);
	}

	// キャラクタ・セットを設定
	if(isset($conn['char'])) {
		$sql = "set character set {$conn['char']}";
		$res = mysqli_query($db, $sql);
		if(!$res) {
			$info['error']('キャラクタ・セットを設定できませんでした。');
			return(NULL);
		}
	}

	// DB状態を更新
	db_status($db, 'conn' , $conn);
	db_status($db, 'error', $info['error']);
	db_status($db, 'begin', FALSE);

	// DB接続リソースを返す
	return($db);
}

/*******************************************************************************
	DB切断
--------------------------------------------------------------------------------
	$ok = db_close($db);
--------------------------------------------------------------------------------
	$ok					正常終了状態
	$db					DB接続リソース
--------------------------------------------------------------------------------
　DB接続をクローズする。
*******************************************************************************/
function db_close($db)
{
	// DB接続をクローズ
	$ok = mysqli_close($db);

	// 正常終了状態を返す
	return($ok);
}

